async function fetchAllRecords(baseUrl, token, tableId) {
  const url = new URL(`${baseUrl}/api/v2/tables/${tableId}/records`);
  url.searchParams.set('limit', '200');
  const response = await apiFetch(url, token);
  return response.list ?? [];
}

async function main() {
  const { baseUrl, token } = await loadConfig();
  const tablesResponse = await apiFetch(`${baseUrl}/api/v2/meta/bases/${BASE_ID}/tables`, token);
  const tables = new Map((tablesResponse.list ?? []).map((table) => [table.title, table.id]));

  for (const [tableTitle, updatesByKey] of Object.entries(tableUpdates)) {
    const tableId = tables.get(tableTitle);
    if (!tableId) {
      console.log(`Skipping ${tableTitle}: table not found`);
      continue;
    }

    const records = await fetchAllRecords(baseUrl, token, tableId);
    const byDeviceKey = new Map(records.map((record) => [record.device_key, record]));
    const patchPayload = [];

    for (const [deviceKey, fieldUpdates] of Object.entries(updatesByKey)) {
      const record = byDeviceKey.get(deviceKey);
      if (!record) {
        console.log(`Skipping ${tableTitle}.${deviceKey}: record not found`);
        continue;
      }

      patchPayload.push({ Id: record.Id, ...fieldUpdates });
    }

    if (!patchPayload.length) {
      console.log(`${tableTitle}: nothing to update`);
      continue;
    }

    if (!APPLY) {
      console.log(`${tableTitle}: would update ${patchPayload.length} record(s)`);
      continue;
    }

    await apiFetch(`${baseUrl}/api/v2/tables/${tableId}/records`, token, {
      method: 'PATCH',
      body: JSON.stringify(patchPayload),
    });

    console.log(`${tableTitle}: updated ${patchPayload.length} record(s)`);
  }

  console.log(APPLY ? 'Research-based population applied.' : 'Dry run complete. Re-run with --apply to write updates.');
}

main().catch((error) => {
  console.error(error.message);
  process.exitCode = 1;
});
